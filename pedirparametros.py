def pedir_parámetros():
    """
    INPUT none
    OUTPUT parametros de las funciones lineales.
    type float
    """
    a = float(input("Ingrese un número para la pendiente de la función: "))
    b = float(input("Ingrese un número para la ordenada al origen de la función: "))
    
    return a, b
    